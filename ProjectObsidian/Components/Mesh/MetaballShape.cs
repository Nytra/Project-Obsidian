public class MetaballShape : MeshXShape
{
    public List<MetaballPoint> Points;

    public Slot OriginSlot;

    public float Threshold;
    public float3 FieldSize;
    public int Resolution;

    private int vertIndex;

    public bool scheduleRangeDatasRecompute;

    private class RangeData
    {
        public float effectiveRange;
        public float3 position;
    }

    private List<RangeData> rangeDatas = new();

    public MetaballShape(MeshX mesh) : base(mesh)
    {
    }

    private void ComputeRangeData()
    {
        if (rangeDatas.Count != Points.Count)
        {
            rangeDatas.Clear();
            for (int i = 0; i < Points.Count; i++)
                rangeDatas.Add(new RangeData());
        }
        int j = 0;
        foreach (var point in Points) // reading from data model here might be bad?
        {
            var data = rangeDatas[j];
            if (point == null || point.IsRemoved)
            {
                data.effectiveRange = 0;
            }
            else
            {
                data.effectiveRange = point.Radius.Value + point.Strength.Value; // this formula may need tweaking?
                data.position = OriginSlot.GlobalPointToLocal(point.Slot.GlobalPosition);
            }
            j++;
        }
        scheduleRangeDatasRecompute = false;
    }

    public float SampleField(float3 point)
    {
        float value = 0;
        foreach (var metaball in Points) // reading from data model here might be bad?
        {
            if (metaball == null || metaball.IsRemoved) continue;
            value += metaball.GetValue(point, OriginSlot);
        }
        return value;
    }

    public float3 CalculateNormal(float3 point, float delta = 0.01f)
    {
        float dx = SampleField(point + new float3(delta, 0, 0)) -
                   SampleField(point - new float3(delta, 0, 0));
        float dy = SampleField(point + new float3(0, delta, 0)) -
                   SampleField(point - new float3(0, delta, 0));
        float dz = SampleField(point + new float3(0, 0, delta)) -
                   SampleField(point - new float3(0, 0, delta));

        float3 normal = new float3(dx, dy, dz);
        return normal.Normalized;
    }

    private void MarchCube(float3 cubePos, float3 step)
    {
        // 8 corners of the cube
        float3[] corners = new float3[8]
        {
            cubePos,
            cubePos + new float3(step.X, 0, 0),
            cubePos + new float3(step.X, step.Y, 0),
            cubePos + new float3(0, step.Y, 0),
            cubePos + new float3(0, 0, step.Z),
            cubePos + new float3(step.X, 0, step.Z),
            cubePos + new float3(step.X, step.Y, step.Z),
            cubePos + new float3(0, step.Y, step.Z)
        };

        bool hasPositive = false, hasNegative = false;

        // Sample field at each corner
        float[] values = new float[8];
        for (int i = 0; i < 8; i++)
        {
            values[i] = SampleField(corners[i]);
            if (values[i] >= Threshold) hasPositive = true;
            else hasNegative = true;
        }

        // Skip cubes that are entirely inside or outside
        if (!hasPositive || !hasNegative) return;

        // Determine cube configuration
        int cubeIndex = 0;
        for (int i = 0; i < 8; i++)
        {
            if (values[i] < Threshold) cubeIndex |= (1 << i);
        }

        // Get triangulation for this configuration
        int[] triangulation = MarchingCubesTable.GetTriangulation(cubeIndex);

        for (int i = 0; i < triangulation.Length; i += 3)
        {
            if (triangulation[i] == -1) break;

            // Create triangle vertices
            for (int j = 0; j < 3; j++)
            {
                int edgeIndex = triangulation[i + j];
                float3 vertPos = InterpolateVertex(corners, values, edgeIndex);
                float3 normal = CalculateNormal(vertPos);
                Vertex v;
                if (Mesh.VertexCount < vertIndex + 1)
                {
                    v = Mesh.AddVertex();
                }
                else
                {
                    v = Mesh.GetVertex(vertIndex);
                }
                v.Position = vertPos;
                v.Normal = normal;
                vertIndex++;
            }
            if (Mesh.TotalTriangleCount < (vertIndex / 3))
            {
                Mesh.AddTriangle(Mesh.GetVertex(Mesh.VertexCount - 3), Mesh.GetVertex(Mesh.VertexCount - 2), Mesh.GetVertex(Mesh.VertexCount - 1));
            }
            else
            {
                var tri = Mesh.GetTriangle((vertIndex / 3) - 1);
                tri.Vertex0 = Mesh.GetVertex(vertIndex - 3);
                tri.Vertex1 = Mesh.GetVertex(vertIndex - 2);
                tri.Vertex2 = Mesh.GetVertex(vertIndex - 1);
            }
        }
    }

    private float3 InterpolateVertex(float3[] corners, float[] values, int edgeIndex)
    {
        // Edge connections in marching cubes
        int[,] edges = new int[12, 2]
        {
            {0,1}, {1,2}, {2,3}, {3,0}, // bottom face
            {4,5}, {5,6}, {6,7}, {7,4}, // top face
            {0,4}, {1,5}, {2,6}, {3,7}  // vertical edges
        };

        int v1 = edges[edgeIndex, 0];
        int v2 = edges[edgeIndex, 1];

        float val1 = values[v1];
        float val2 = values[v2];

        if (Math.Abs(Threshold - val1) < 0.00001f) return corners[v1];
        if (Math.Abs(Threshold - val2) < 0.00001f) return corners[v2];
        if (Math.Abs(val1 - val2) < 0.00001f) return corners[v1];

        float t = (Threshold - val1) / (val2 - val1);
        return MathX.Lerp(corners[v1], corners[v2], t);
    }

    private void Generate(MeshX mesh)
    {
        // might be better to do this in PrepareAssetUpdateData?
        if (scheduleRangeDatasRecompute)
            ComputeRangeData();

        float3 step = FieldSize / Resolution;

        for (int x = 0; x < Resolution - 1; x++)
        {
            for (int y = 0; y < Resolution - 1; y++)
            {
                for (int z = 0; z < Resolution - 1; z++)
                {
                    float3 cubePos = new float3(x, y, z) * step - FieldSize * 0.5f;

                    bool anyIn = false;
                    foreach (var rangeData in rangeDatas)
                    {
                        if (rangeData.effectiveRange == 0) continue;
                        if (MathX.DistanceSqr(cubePos, rangeData.position) < MathX.Pow(rangeData.effectiveRange, 2f))
                        {
                            anyIn = true;
                            break;
                        }
                    }
                    if (!anyIn) continue;

                    MarchCube(cubePos, step);
                }
            }
        }
    }

    public override void Update()
    {
        vertIndex = 0;
        Generate(Mesh);
    }
}

